/**
 * Транслитерация кириллицы в латиницу для создания ЧПУ slug'ов
 */
const transliterationMap: Record<string, string> = {
  'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
  'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
  'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
  'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch',
  'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
  'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
  'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
  'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
  'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch',
  'Ъ': '', 'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
};

/**
 * Транслитерирует кириллицу в латиницу
 */
function transliterate(text: string): string {
  return text
    .split('')
    .map(char => transliterationMap[char] || char)
    .join('');
}

/**
 * Создает ЧПУ slug из текста
 * - Транслитерирует кириллицу в латиницу
 * - Удаляет все спецсимволы кроме дефисов
 * - Заменяет пробелы на дефисы
 * - Удаляет множественные дефисы
 * - Обрезает до 100 символов
 */
export function slugify(text: string): string {
  if (!text || typeof text !== 'string') {
    return '';
  }

  return text
    .trim()
    // Транслитерация кириллицы
    .split('')
    .map(char => transliterationMap[char] || char)
    .join('')
    // Приводим к нижнему регистру
    .toLowerCase()
    // Удаляем все символы кроме букв, цифр, пробелов и дефисов
    .replace(/[^a-z0-9\s-]/g, '')
    // Заменяем пробелы на дефисы
    .replace(/\s+/g, '-')
    // Удаляем множественные дефисы
    .replace(/-+/g, '-')
    // Удаляем дефисы в начале и конце
    .replace(/^-+|-+$/g, '')
    // Обрезаем до 100 символов
    .substring(0, 100)
    // Удаляем дефис в конце если он остался после обрезки
    .replace(/-+$/, '');
}


