/**
 * Программные афишные тексты по названию и типу (без LLM): лид, «О событии», подсказки для кикера.
 */

/** @param {string} s */
function norm(s) {
  return String(s || '')
    .trim()
    .replace(/\s+/g, ' ');
}

/** @param {string} s */
export function normalizeTitleKey(s) {
  return norm(s)
    .toLowerCase()
    .replace(/ё/g, 'е')
    .replace(/[«»"""'`]/g, '')
    .replace(/\s+/g, ' ');
}

