/**
 * @param {string} title
 * @returns {{ a: string, b: string } | null}
 */
export function parseMatchupTitle(title) {
  const t = norm(title);
  const parts = t.split(/\s*[—–\-]\s+/).map((x) => x.trim()).filter(Boolean);
  if (parts.length < 2) return null;
  const a = parts[0].replace(/[()]/g, ' ').trim();
  const b = parts.slice(1).join(' — ').replace(/[()]/g, ' ').trim();
  if (a.length < 2 || b.length < 2) return null;
  if (a.length > 85 || b.length > 85) return null;
  return { a, b };
}

/** @param {string | null | undefined} iso */
export function formatRuDateTimeHint(iso) {
  if (!iso || !String(iso).trim()) return null;
  const d = new Date(String(iso));
  if (Number.isNaN(d.getTime())) return null;
  try {
    return d.toLocaleString('ru-RU', {
      weekday: 'long',
      day: 'numeric',
      month: 'long',
      hour: '2-digit',
      minute: '2-digit',
      timeZone: 'Europe/Moscow',
    });
  } catch {
    return null;
  }
}

/**
 * Подстрочник героя: город · дата/время · площадка (что есть в афише).
 * @param {{ ageLimit?: string | null, cityName?: string | null, beginSample?: string | null } | null | undefined} catalogHints
 * @param {string | null | undefined} venueLabel
 */
export function formatCatalogHintsSubline(catalogHints, venueLabel) {
  const h = catalogHints || {};
  const bits = [];
  const city = h.cityName != null ? String(h.cityName).trim() : '';
  if (city) bits.push(city);
  const when = formatRuDateTimeHint(h.beginSample);
  if (when) bits.push(when);
  const venue = venueLabel != null ? String(venueLabel).trim() : '';
  if (venue && !bits.some((b) => b.includes(venue))) bits.push(venue);
  if (bits.length) return bits.join(' · ');
  return venue || null;
}

/** Устаревшая формулировка из старого промпта OpenAI — меняем акцент на место проведения. */
export const LEGACY_HERO_SUBLINE_SCHEDULE_HINT = 'Даты и время — в расписании ниже';

/** Если нет данных о площадке в каталоге — нейтральная отсылка к блоку сеансов. */
export const HERO_SUBLINE_VENUE_FALLBACK =
  'Площадка (театр, стадион, арена) — в блоке сеансов ниже.';

/**
 * Подстрочник героя: без отсылки к «датам в расписании»; приоритет — город, дата сеанса (если есть), площадка.
 * @param {string | null | undefined} heroSubline
 * @param {{ ageLimit?: string | null, cityName?: string | null, beginSample?: string | null } | null | undefined} catalogHints
 * @param {string | null | undefined} venueLabel
 * @returns {string | null}
 */
export function resolveHeroSublineVenueFocused(heroSubline, catalogHints, venueLabel) {
  const raw = heroSubline != null ? String(heroSubline).trim() : '';
  if (!raw) return formatCatalogHintsSubline(catalogHints, venueLabel);

  const legacy = LEGACY_HERO_SUBLINE_SCHEDULE_HINT;
  if (!raw.includes(legacy)) return raw;

  const rest = raw.replace(legacy, '').replace(/^[\s·]+|[\s·]+$/g, '').trim();
  const fromHints = formatCatalogHintsSubline(catalogHints, venueLabel);
  if (fromHints) {
    if (rest && !fromHints.includes(rest) && !rest.includes(fromHints)) {
      return `${fromHints} · ${rest}`;
    }
    return fromHints;
  }
  if (rest) return rest;
  const v = venueLabel != null ? String(venueLabel).trim() : '';
  if (v) return v;
  return HERO_SUBLINE_VENUE_FALLBACK;
}

/**
 * Короткий лид для героя (2–3 предложения).
 * @param {string} title
 * @param {'sport' | 'football' | 'concert' | 'theater' | 'kids' | 'default'} kind
 * @param {string} categoryLabel
 * @param {string | null | undefined} venueLabel
 */
export function buildProgrammaticHeroLead(title, kind, categoryLabel, venueLabel) {
  const t = norm(title) || 'Мероприятие';
  const venue = venueLabel?.trim();
  const known = kind === 'theater' || kind === 'kids' ? findKnownStageWork(t) : null;
  if (known?.paras?.[0]) {
    const one = known.paras[0];
    const short = one.length > 320 ? `${one.slice(0, 317).trim()}…` : one;
    return `${short} Билеты на «${t}» — на этой странице, с выбором мест.`;
  }
  const m = kind === 'football' || kind === 'sport' ? parseMatchupTitle(t) : null;
  if (m) {
    return `«${m.a}» и «${m.b}» выходят друг на друга в матче, где решают концентрация, командная дисциплина и поддержка трибун. Живой стадион — это другой масштаб эмоций по сравнению с экраном. Билеты и зоны — ниже на странице.`;
  }
  if (kind === 'concert') {
    return `«${t}» — повод собраться на живой звук, свет и общую волну зала. Каждый концерт чуть-чуть неповторим: акустика площадки, настрой группы и отклик публики складываются заново. Выберите дату и места на этой странице.`;
  }
  if (kind === 'theater') {
    return `«${t}» — театральный вечер, где смысл держится на актёрской работе, ритме режиссуры и вашем внимании. Приходите за 20–30 минут до начала, чтобы войти в тон спокойно. Билеты и схема зала — ниже.`;
  }
  if (kind === 'kids') {
    return `«${t}» — событие для семейного просмотра: ясный ритм, яркие образы и бережный тон. Заложите время на дорогу и короткую адаптацию у входа — детям спокойнее, когда всё успели без спешки.`;
  }
  if (kind === 'sport' || kind === 'football') {
    return `«${t}» — спортивный формат, где решение принимается в реальном времени, а атмосфера трибун добавляет давления и поддержки одновременно. Проверьте зону и время на странице билета.`;
  }
  const place = venue ? ` Площадка: «${venue}».` : '';
  return `«${t}» в категории «${categoryLabel}».${place} На странице — даты, места и оформление без очередей.`;
}

