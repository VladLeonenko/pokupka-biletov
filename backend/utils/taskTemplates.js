// Получить шаблон задач по slug продукта или названию
export function getTaskTemplateByProduct(productSlug, productTitle) {
  const slugLower = (productSlug || '').toLowerCase();
  const titleLower = (productTitle || '').toLowerCase();

  // Прямое совпадение по slug
  if (TASK_TEMPLATES[slugLower]) {
    return TASK_TEMPLATES[slugLower];
  }

  // Поиск по ключевым словам в названии
  if (titleLower.includes('лендинг') || titleLower.includes('landing')) {
    return TASK_TEMPLATES['landing'];
  }
  if (titleLower.includes('корпоративн') || titleLower.includes('corporate')) {
    return TASK_TEMPLATES['corporate-website'];
  }
  if (titleLower.includes('магазин') || titleLower.includes('shop') || titleLower.includes('e-commerce')) {
    return TASK_TEMPLATES['online-shop'];
  }
  if (titleLower.includes('seo') && titleLower.includes('аудит')) {
    return TASK_TEMPLATES['seo-audit-sayta'];
  }
  if (titleLower.includes('seo') || titleLower.includes('продвижен')) {
    return TASK_TEMPLATES['prodvizhenie-sayta-seo'];
  }
  if (titleLower.includes('контекстн') || titleLower.includes('реклам')) {
    return TASK_TEMPLATES['kontekstnaya-reklama'];
  }
  if (titleLower.includes('логотип') || titleLower.includes('фирменн')) {
    return TASK_TEMPLATES['dizayn-logotipa-i-firmennogo-stilya'];
  }
  if (titleLower.includes('ui') || titleLower.includes('ux') || titleLower.includes('дизайн интерфейс')) {
    return TASK_TEMPLATES['professionalnyy-uiux-dizayn-interfeysov-dlya-vashego-biznesa'];
  }
  if (titleLower.includes('поддержк') || titleLower.includes('support')) {
    return TASK_TEMPLATES['tehnicheskaya-podderzhka-sayta'];
  }
  if (titleLower.includes('ai') && titleLower.includes('продвижен')) {
    return TASK_TEMPLATES['ai-prodvizhenie'];
  }
  if (titleLower.includes('маркетинг') && titleLower.includes('продаж')) {
    return TASK_TEMPLATES['marketing-prodazhi'];
  }
  if (titleLower.includes('tilde')) {
    return TASK_TEMPLATES['razrabotka-saytov-na-tilde'];
  }
  if (titleLower.includes('wordpress') || titleLower.includes('wp')) {
    return TASK_TEMPLATES['wordpress'];
  }
  if (titleLower.includes('битрикс') || titleLower.includes('bitrix')) {
    return TASK_TEMPLATES['bitrix'];
  }
  if (titleLower.includes('pr') || titleLower.includes('пиар')) {
    return TASK_TEMPLATES['uslugi-pr'];
  }
  if (titleLower.includes('аутсорс') || titleLower.includes('outsource')) {
    return TASK_TEMPLATES['autsorsing-digital-agentstva-professionalnye-uslugi-dlya-vashego-biznesa'];
  }
  if (titleLower.includes('ai') && titleLower.includes('решен')) {
    return TASK_TEMPLATES['innovatsionnye-ai-resheniya-dlya-optimizatsii-biznesa'];
  }

  // Если ничего не найдено, возвращаем общий шаблон
  return [
    { title: 'Анализ требований', description: 'Изучить требования проекта', priority: 'high' },
    { title: 'Планирование', description: 'Составить план работ и этапы', priority: 'high' },
    { title: 'Разработка', description: 'Выполнить основную разработку', priority: 'high' },
    { title: 'Тестирование', description: 'Провести тестирование', priority: 'medium' },
    { title: 'Запуск', description: 'Запустить проект', priority: 'high' },
  ];
}

// Применить шаблон задач к существующей сделке
export async function applyTaskTemplateToDeal(dealId, productSlug, productTitle, createdBy) {
  try {
    if (!dealId) {
      console.error('[taskTemplates] dealId is required');
      return { success: false, error: 'dealId is required' };
    }

    const templates = getTaskTemplateByProduct(productSlug, productTitle);
    
    if (!templates || templates.length === 0) {
      return { success: false, error: 'Template not found' };
    }

    const createdTasks = [];
    for (const template of templates) {
      const result = await pool.query(
        `INSERT INTO tasks (title, description, status, priority, deal_id, created_by, category, created_at, updated_at)
         VALUES ($1, $2, $3, $4, $5, $6, $7, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)
         RETURNING id`,
        [
          template.title,
          template.description,
          'new',
          template.priority || 'medium',
          dealId,
          createdBy || null,
          'development',
        ]
      );
      createdTasks.push(result.rows[0].id);
    }

    console.log(`[taskTemplates] Applied template with ${templates.length} tasks to deal ${dealId}`);
    return { success: true, tasksCreated: createdTasks.length, taskIds: createdTasks };
  } catch (error) {
    console.error('[taskTemplates] Error applying template:', error);
    return { success: false, error: error.message };
  }
}


