-- Курсы обучения: заглавная → страницы → тест
-- Каждый курс — отдельная страница со своей шкалой прогресса

CREATE TABLE IF NOT EXISTS training_courses (
  id SERIAL PRIMARY KEY,
  slug TEXT NOT NULL UNIQUE,
  title TEXT NOT NULL,
  cover_description TEXT,
  estimated_test_minutes INTEGER DEFAULT 10,
  sort_order INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_training_courses_slug ON training_courses(slug);

-- Страницы курса: cover | objection | content
-- objection — ссылка на sales_training_materials (material_id)
-- content — произвольный текст (title, content)
CREATE TABLE IF NOT EXISTS training_course_pages (
  id SERIAL PRIMARY KEY,
  course_id INTEGER NOT NULL REFERENCES training_courses(id) ON DELETE CASCADE,
  page_index INTEGER NOT NULL,
  page_type TEXT NOT NULL CHECK (page_type IN ('cover', 'objection', 'content')),
  title TEXT,
  content TEXT,
  objection_text TEXT,
  solution_text TEXT,
  material_id INTEGER REFERENCES sales_training_materials(id) ON DELETE SET NULL,
  UNIQUE(course_id, page_index)
);
CREATE INDEX IF NOT EXISTS idx_course_pages_course ON training_course_pages(course_id);

-- Прогресс по курсам (страницы + тест)
CREATE TABLE IF NOT EXISTS manager_course_progress (
  user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  course_slug TEXT NOT NULL,
  last_page_index INTEGER DEFAULT 0,
  completed_page_count INTEGER DEFAULT 0,
  test_passed BOOLEAN DEFAULT FALSE,
  test_score INTEGER,
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  PRIMARY KEY (user_id, course_slug)
);
CREATE INDEX IF NOT EXISTS idx_course_progress_user ON manager_course_progress(user_id);

