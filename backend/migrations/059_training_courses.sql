-- Открытый вопрос: correct_index = -1 для free-text (ослабляем constraint)
DO $$
DECLARE
  cname TEXT;
BEGIN
  SELECT conname INTO cname FROM pg_constraint c
  JOIN pg_attribute a ON a.attrelid = c.conrelid AND a.attnum = ANY(c.conkey)
  WHERE c.conrelid = 'sales_training_questions'::regclass AND c.contype = 'c' AND a.attname = 'correct_index' LIMIT 1;
  IF cname IS NOT NULL THEN
    EXECUTE format('ALTER TABLE sales_training_questions DROP CONSTRAINT %I', cname);
  END IF;
  ALTER TABLE sales_training_questions ADD CONSTRAINT sales_training_questions_correct_index_check CHECK (correct_index >= -1);
EXCEPTION
  WHEN duplicate_object THEN NULL; -- constraint уже есть
END $$;

