/**
 * Улучшает описание кейса
 */
function improveSummary(caseData) {
  const { title, summary, category } = caseData;
  
  // Если summary уже достаточно длинное (больше 200 символов), оставляем как есть
  if (summary && summary.length > 200) {
    return summary;
  }
  
  // Генерируем расширенное краткое описание
  const { story } = generateExtendedDescription(caseData);
  
  // Берем первые 2-3 предложения из истории для summary
  const sentences = story.split(/[.!?]+/).filter(s => s.trim().length > 20);
  const shortSummary = sentences.slice(0, 2).join('. ').trim();
  
  return shortSummary + (shortSummary.length < 150 ? '. ' + (summary || '') : '');
}

/**
 * Основная функция для улучшения описаний кейсов
 */
async function improveCaseDescriptions() {
  try {
    console.log('📝 Начинаем улучшение описаний кейсов...\n');
    
    // Получаем все опубликованные кейсы
    const result = await pool.query(
      `SELECT slug, title, summary, content_html, category, tools, donor_url 
       FROM cases 
       WHERE is_published = TRUE 
       ORDER BY updated_at DESC`
    );
    
    console.log(`Найдено ${result.rows.length} кейсов для обработки\n`);
    
    let updated = 0;
    let skipped = 0;
    
    for (const row of result.rows) {
      try {
        const caseData = {
          title: row.title,
          summary: row.summary,
          category: row.category,
          tools: row.tools || [],
          donorUrl: row.donor_url
        };
        
        // Генерируем улучшенные описания
        const improvedSummary = improveSummary(caseData);
        const contentHtml = generateContentHtml(caseData);
        
        // Обновляем только если summary короткое или content_html пустой
        const shouldUpdate = (!row.summary || row.summary.length < 200) || !row.content_html;
        
        if (shouldUpdate) {
          await pool.query(
            `UPDATE cases 
             SET summary = $1, 
                 content_html = $2,
                 updated_at = NOW() 
             WHERE slug = $3`,
            [improvedSummary, contentHtml, row.slug]
          );
          
          console.log(`✅ Обновлен: ${row.title}`);
          console.log(`   Slug: ${row.slug}`);
          updated++;
        } else {
          console.log(`⏭️  Пропущен (уже есть описание): ${row.title}`);
          skipped++;
        }
        
      } catch (error) {
        console.log(`❌ Ошибка при обработке ${row.slug}: ${error.message}`);
      }
    }
    
    console.log(`\n🎉 Готово!`);
    console.log(`   Обновлено: ${updated}`);
    console.log(`   Пропущено: ${skipped}`);
    
    process.exit(0);
  } catch (error) {
    console.error('❌ Критическая ошибка:', error.message);
    console.error(error);
    process.exit(1);
  }
}

// Запуск скрипта
improveCaseDescriptions();

