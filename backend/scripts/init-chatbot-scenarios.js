async function initChatbotScenarios() {
  try {
    console.log('🚀 Инициализация базовых сценариев чат-бота...');

    // Удаляем старые правила (опционально, можно закомментировать)
    // await pool.query('DELETE FROM chatbot_rules WHERE name LIKE $1', ['%базовый%']);

    let inserted = 0;
    let updated = 0;

    for (const scenario of chatbotScenarios) {
      // Проверяем, существует ли уже правило с таким именем
      const existing = await pool.query(
        'SELECT id FROM chatbot_rules WHERE name = $1',
        [scenario.name]
      );

      if (existing.rows.length > 0) {
        // Обновляем существующее правило
        await pool.query(
          `UPDATE chatbot_rules 
           SET keywords = $1, response_text = $2, response_type = $3, 
               priority = $4, is_active = $5
           WHERE id = $6`,
          [
            scenario.keywords,
            scenario.response_text,
            scenario.response_type,
            scenario.priority,
            scenario.is_active,
            existing.rows[0].id
          ]
        );
        updated++;
        console.log(`✅ Обновлено: ${scenario.name}`);
      } else {
        // Создаем новое правило
        await pool.query(
          `INSERT INTO chatbot_rules (name, keywords, response_text, response_type, priority, is_active)
           VALUES ($1, $2, $3, $4, $5, $6)`,
          [
            scenario.name,
            scenario.keywords,
            scenario.response_text,
            scenario.response_type,
            scenario.priority,
            scenario.is_active
          ]
        );
        inserted++;
        console.log(`✅ Создано: ${scenario.name}`);
      }
    }

    console.log(`\n✨ Готово! Создано: ${inserted}, Обновлено: ${updated}`);
    console.log(`📊 Всего сценариев: ${chatbotScenarios.length}`);
  } catch (error) {
    console.error('❌ Ошибка при инициализации сценариев:', error);
    process.exit(1);
  } finally {
    await pool.end();
  }
}

initChatbotScenarios();

