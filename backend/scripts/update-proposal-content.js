async function updateProposal() {
  try {
    console.log(`📦 Обновление коммерческого предложения ID: ${proposalId}...`);

    // Удаляем старые слайды
    await pool.query('DELETE FROM proposal_slides WHERE proposal_id = $1', [proposalId]);
    console.log('✅ Старые слайды удалены');

    // Вставляем новые слайды
    for (const slide of updatedSlides) {
      await pool.query(
        `INSERT INTO proposal_slides (proposal_id, slide_type, sort_order, content)
         VALUES ($1, $2, $3, $4::jsonb)`,
        [proposalId, slide.slideType, slide.sortOrder, JSON.stringify(slide.content)]
      );
    }

    console.log(`✅ Обновлено ${updatedSlides.length} слайдов`);
    console.log(`\n🎉 Готово! Предложение обновлено с полным контентом из референса`);
    console.log(`📄 Откройте в админке: /admin/commercial-proposals/${proposalId}`);

  } catch (err) {
    console.error('❌ Ошибка:', err.message);
    console.error(err);
    process.exit(1);
  } finally {
    await pool.end();
  }
}

